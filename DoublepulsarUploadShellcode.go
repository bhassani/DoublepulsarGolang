func calculateDoublepulsarArch(s uint64) string {
	if s&0xffffffff00000000 == 0 {
		return "x86 (32-bit)"
	}
	return "x64 (64-bit)"
}

func ByteXor(data []byte, key uint32) []byte {
	// Convert key to 4-byte little-endian array
	keyBytes := make([]byte, 4)
	binary.LittleEndian.PutUint32(keyBytes, key)

	keyLength := len(keyBytes)

	// XOR operation
	result := make([]byte, len(data))
	for i := 0; i < len(data); i++ {
		result[i] = data[i] ^ keyBytes[i%keyLength]
	}

	return result
}

func CalculateDoublePulsarXorKey(s uint32) uint32 {
	part1 := (s & 0xff00) | (s << 16)
	part2 := (s >> 16) | (s & 0xff0000)

	x := 2*s ^ ((part1 << 8) | (part2 >> 8))

	return x & 0xffffffff
}

func calculateDoublepulsarXorKeyTwo(s uint32) uint32 {
	x := (2 * s) ^ (((s&0xff00 | (s << 16)) << 8) | (((s >> 16) | s&0xff0000) >> 8))
	return x & 0xffffffff
}

