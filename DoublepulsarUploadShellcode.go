func (p *SmbDoublePulsarExecPacket) ToBytes() ([]byte, error) {
	buf := new(bytes.Buffer)

	write := func(data interface{}) {
		binary.Write(buf, binary.LittleEndian, data)
	}

	write(p.SmbMessageType)
	write(p.SmbMessageLength)

	buf.Write(p.ProtocolHeader[:])
	buf.WriteByte(p.SmbCommand)

	write(p.NtStatus)
	buf.WriteByte(p.Flags)
	write(p.Flags2)
	write(p.ProcessIDHigh)
	buf.Write(p.Signature[:])

	write(p.Reserved)
	write(p.TreeId)
	write(p.ProcessID)
	write(p.UserID)
	write(p.MultipleID)

	buf.WriteByte(p.WordCount)
	write(p.TotalParameterCount)
	write(p.TotalDataCount)
	write(p.MaxParameterCount)
	write(p.MaxDataCount)
	buf.WriteByte(p.MaxSetupCount)

	buf.WriteByte(p.Reserved1)
	write(p.Flags1)
	write(p.Timeout)
	write(p.Reserved2)

	write(p.ParameterCount)
	write(p.ParamOffset)
	write(p.DataCount)
	write(p.DataOffset)

	buf.WriteByte(p.SetupCount)
	buf.WriteByte(p.Reserved3)
	write(p.Subcommand)

	write(p.ByteCount)
	buf.WriteByte(p.Padding)

	buf.Write(p.SessionSetupParameters[:])
	buf.Write(p.SMBPayload[:])

	return buf.Bytes(), nil
}

