// rotate right (32-bit)
func ror(value uint32, bits uint) uint32 {
	return (value >> bits) | (value << (32 - bits))
}

// generate process hash
func generateProcessHash(process string) uint32 {
	var procHash uint32 = 0
	proc := process + "\x00"

	for i := 0; i < len(proc); i++ {
		procHash = ror(procHash, 13)
		procHash += uint32(proc[i])
	}

	return procHash
}

